// Imputs two numbers and prints their sum and product

#include <stdio.h>
int main()
{
    int a = 0, b = 0;
    scanf("%d %d", &a, &b);           // reads two numbers
    printf("Sum is %d\n", a + b);     // prints their sum
    printf("Product is %d\n", a * b); // prints their product
    return 0;
}
